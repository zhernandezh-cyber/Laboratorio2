    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String usuario = jTextField1.getText().trim();
        String clave = new String(jPasswordField1.getPassword());

        if (usuario.isEmpty() || clave.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar usuario y contraseña.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (usuario.equals("admin") && clave.equals("Umariano24")) {
            MenuPrincipal menu = new MenuPrincipal();
            menu.setVisible(true);
            this.dispose();
        } else {
            intentosFallidos++;
            int restantes = 3 - intentosFallidos;
            if (intentosFallidos >= 3) {
                JOptionPane.showMessageDialog(this, "Usuario bloqueado por demasiados intentos.", "Bloqueado", JOptionPane.ERROR_MESSAGE);
                jButton1.setEnabled(false);
                jTextField1.setEnabled(false);
                jPasswordField1.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Credenciales incorrectas. Intentos restantes: " + restantes, "Error", JOptionPane.ERROR_MESSAGE);
                jPasswordField1.setText("");
            }
        }
    }// TODO add your handling code here:

